    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        // TODO add your handling code here:
        String usuario =  txtUsuario.getText();
         String contrasema =  txtContrasena.getText();
         String rango =  txtRango.getText();
         
     
        if (usuario.isEmpty() || contrasema.isEmpty() || rango.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
         } else {
          Usuario obj = new Usuario();
        if (Usuario.verificarUsuario(usuario) == -1) {
            obj.setUsuario(usuario);
            obj.setContrasena(contrasema);
            obj.setRango(rango);       
            ListaUsuario.agregar(obj);
            JOptionPane.showMessageDialog(this, "Bienvenido " + usuario);
        } else {
            JOptionPane.showMessageDialog(this, "Este usuario ya se encuentra disponible.");
        }
    }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

